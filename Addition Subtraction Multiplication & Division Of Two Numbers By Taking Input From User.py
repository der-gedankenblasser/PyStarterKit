#Add, Sub, Multi & Div OF Two Numbers By Taking Input From User

num1 = input('Enter First Number: ')
num2 = input('Enter Second Number: ')
add = int(num1) + int(num2)
sub = int(num1) - int(num2)
multi = int(num1) * int(num2)
div = int(num1) / int(num2)
print('The Add Of Two Numbers Is: ', add)
print('The Sub Of Two Numbers Is: ', sub)
print('The Multi Of Two Numbers Is: ', multi)
print('The Div Of Two Numbers Is: ', div)